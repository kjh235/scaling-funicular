if __name__ == '__main__':
    import json
    import sys

    config_path = sys.argv[1] if len(sys.argv) > 1 else 'sample-config.json'
    year        = int(sys.argv[2]) if len(sys.argv) > 2 else 2026

    with open(config_path) as f:
        cfg = json.load(f)

    resolver = HolidayResolver(cfg)
    events   = resolver.resolve(year)

    print(f"\n{'─'*64}")
    print(f"  Resolved {len(events)} holidays for {year}")
    print(f"{'─'*64}\n")

    for ev in events:
        shift = ''
        if ev['is_shifted']:
            shift = f"  (actual: {ev['actual_date']} → {ev['shift_direction']})"
        jur = ev['jurisdiction']
        jur_tag = f"[{jur.get('iso_3166_2') or jur.get('iso_3166_1') or jur['id']}]" if jur else ''
        max_level = 'none'
        for imp in ev['impacts']:
            lvl = (imp.get('operational_impact') or {}).get('impact_level', '')
            order = {'full': 3, 'conditional': 2, 'partial': 1, 'none': 0}
            if order.get(lvl, -1) > order.get(max_level, -1):
                max_level = lvl
        print(f"  {ev['observed_date']}  {jur_tag:<8}  {ev['name']:<42}  [{max_level}]{shift}")

    print()
